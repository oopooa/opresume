/**
 * AI 简历解析 Prompt 模板
 *
 * 指导 AI 从 PDF 文本中提取结构化简历数据，
 * 严格遵循 JSON Resume 标准格式输出。
 */

/** 系统提示词 */
export const SYSTEM_PROMPT = `你是一个专业的简历解析助手。你的任务是从用户提供的简历文本中提取结构化信息，并严格按照指定的 JSON 格式输出。

输出要求：
1. 必须返回且仅返回一个有效的 JSON 对象，不要包含任何其他文本说明
2. 所有日期使用 YYYY-MM 格式（如 "2021-03"），如果是"至今"则使用 "present"
3. 工作描述和项目内容：如果原文是分条列举的，使用字符串数组（每条一个元素）；如果原文是连续段落，使用普通字符串
4. 如果某个字段无法从原文中提取，则省略该字段（不要使用 null 或空字符串）
5. 工作经历、教育经历等按时间倒序排列（最新的在前）
6. 技能列表：仅当原文中的技能是"技能名称+熟练程度"的逐条格式时才提取为 skills 数组，level 使用中文（精通/熟练/了解）；如果原文是分类描述、纯文本罗列或技术栈概述等非逐条格式，则省略 skills 字段
7. 不要编造任何原文中不存在的信息`;

/** JSON 模板 */
const JSON_TEMPLATE = `{
  "basics": {
    "name": "姓名",
    "email": "邮箱",
    "phone": "手机号",
    "label": "职位头衔",
    "summary": "个人简介/自我评价",
    "location": {
      "city": "城市"
    },
    "workExpYear": "工作年限（如 5）",
    "extraFields": [
      { "key": "字段名", "value": "字段值" }
    ]  // 简历中除姓名、邮箱、手机号、职位、城市之外的其他联系方式或个人信息（如微信、GitHub、个人网站、博客等），每项一个对象
  },
  "work": [
    {
      "name": "公司名称",
      "position": "职位",
      "department": "部门（如有）",
      "startDate": "2021-03",
      "endDate": "2024-01",
      "summary": ["工作职责或成果要点1", "工作职责或成果要点2"]  // 分条列举时用数组，连续段落时用字符串
    }
  ],
  "education": [
    {
      "institution": "学校名称",
      "area": "专业",
      "studyType": "学历（如 本科/硕士/博士）",
      "startDate": "2017-09",
      "endDate": "2021-06"
    }
  ],
  "projects": [
    {
      "name": "项目名称",
      "description": "一句话项目简介",
      "content": ["项目详细内容要点1", "项目详细内容要点2"]  // 分条列举时用数组，连续段落时用字符串,
      "roles": ["担任角色"],
      "startDate": "2022-01",
      "endDate": "2023-06"
    }
  ],
  "skills": [
    {
      "name": "技能名称",
      "level": "熟练"
    }
  ]  // level 使用中文：精通/熟练/了解。仅当原文是逐条"技能+熟练度"格式时才输出，否则省略整个 skills,
  "awards": [
    {
      "title": "奖项名称",
      "date": "2023-06",
      "awarder": "颁发机构"
    }
  ]
}`;

/**
 * 构建用户提示词
 * @param pdfText PDF 提取的文本
 * @returns 完整的用户提示词
 */
export function buildUserPrompt(pdfText: string): string {
  return `请从以下简历文本中提取信息，并严格按照 JSON 格式输出。

简历文本：
---
${pdfText}
---

请提取以下信息（如果存在）：
- 基本信息（姓名、邮箱、手机号、职位头衔、所在城市、工作年限、个人简介，以及其他联系方式如微信、GitHub、网站等）
- 工作经历（公司、部门、职位、时间、工作描述）
- 教育背景（学校、专业、学历、时间）
- 项目经验（项目名称、一句话简介、详细内容、角色、时间）
- 技能列表（技能名称、熟练程度、关键词）
- 获奖经历（奖项、时间、颁发机构）

输出格式：
${JSON_TEMPLATE}

注意：只返回 JSON 对象，不要添加 markdown 代码块标记或其他文字。`;
}
